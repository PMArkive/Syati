#include "syati.h"

/*********************************************************************************************************************/
/* Enable crash debugger                                                                                             */
/*********************************************************************************************************************/
kmWrite32(0x804B7D90, 0x60000000);
kmWrite32(0x804B7E54, 0x60000000);
kmWrite32(0x805B66B4, 0x60000000);


/*********************************************************************************************************************/
/* Call static constructors                                                                                          */
/*********************************************************************************************************************/
typedef void (*Func)();
extern Func __ctor_loc;
extern Func __ctor_end;

void init() {
    for (Func* f = &__ctor_loc; f < &__ctor_end; f++)
        (*f)();
}

#if defined(TWN) || defined(KOR)
    kmBranch(0x804B6758, init);
#else
    kmBranch(0x804B66E8, init);
#endif
